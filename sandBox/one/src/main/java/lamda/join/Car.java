package lamda.join;

public class Car {

		private String name;
		private int colorId;
		
		
		public Car(String name, int colorId) {
			super();
			this.name = name;
			this.colorId = colorId;
		}
		
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getColorId() {
			return colorId;
		}
		public void setColorId(int colorId) {
			this.colorId = colorId;
		}
		
}
